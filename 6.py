'''6. Escribe un programa que utilice operadores
de asignación para realizar operaciones como 
incrementar o decrementar el valor de una variable.'''

num1 = int(input("Ingresa un número: "))
print(f"Valor inicial: {num1}")
num1 += 5
print(f"Después de incrementar en 5: {num1}")
num1 -= 3
print(f"Después de decrementar en 3: {num1}")
num1 *= 2
print(f"Después de multiplicar por 2: {num1}")
num1 /= 4
print(f"Después de dividir por 4: {num1}")